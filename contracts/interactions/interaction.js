async function main() {
  // Connect to the deployed contract
  const greenToken = new ethers.Contract(contractAddress, abi, wallet);

  // Example: Get the token name
  const name = await greenToken.name();
  console.log("Token Name:", name);

  // Example: Get the token symbol
  const symbol = await greenToken.symbol();
  console.log("Token Symbol:", symbol);

  // Example: Get the total supply
  const totalSupply = await greenToken.totalSupply();
  console.log("Total Supply:", ethers.utils.formatUnits(totalSupply, 18));

  // Example: Transfer tokens
  const recipient = "0xB8Cf1d00F959F0F15e21c961814727dEd0aAd139";
  const amount = ethers.utils.parseUnits("1", 18); // Assuming 18 decimals
  const transferTx = await greenToken.transfer(recipient, amount);
  await transferTx.wait();
  console.log(`Transferred ${amount.toString()} tokens to ${recipient}`);
}

